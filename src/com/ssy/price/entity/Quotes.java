package com.ssy.price.entity;

import java.io.Serializable;

@SuppressWarnings("serial")
public class Quotes implements Serializable {
	Integer pid;// 产品id
	String pname;// 品名
	String parea;// 产地
	String spec;// 规格
	Double price;// 价格
	String pdate;// 日期
	Integer cCount;// 评论总数
	String imgUrl;// 缩略图地址
	String alias;// 别名
	Double tradeNum;// 交易量
	String desc;// 说明
	Double change;// 涨跌值 正数为涨，负数为跌，0.00为不涨跌
	Integer viewPer;// 查看权限 0:不可查看;1:可查看
	Integer isCollect;// 是否收藏 0:未收藏;1:已收藏

	public Integer getPid() {
		return pid;
	}

	public void setPid(Integer pid) {
		this.pid = pid;
	}

	public String getPname() {
		return pname;
	}

	public void setPname(String pname) {
		this.pname = pname;
	}

	public String getParea() {
		return parea;
	}

	public void setParea(String parea) {
		this.parea = parea;
	}

	public String getSpec() {
		return spec;
	}

	public void setSpec(String spec) {
		this.spec = spec;
	}

	public Double getPrice() {
		return price;
	}

	public void setPrice(Double price) {
		this.price = price;
	}

	public String getPdate() {
		return pdate;
	}

	public void setPdate(String pdate) {
		this.pdate = pdate;
	}

	public String getImgUrl() {
		return imgUrl;
	}

	public void setImgUrl(String imgUrl) {
		this.imgUrl = imgUrl;
	}

	public String getAlias() {
		return alias;
	}

	public void setAlias(String alias) {
		this.alias = alias;
	}

	public Double getTradeNum() {
		return tradeNum;
	}

	public void setTradeNum(Double tradeNum) {
		this.tradeNum = tradeNum;
	}

	public String getDesc() {
		return desc;
	}

	public void setDesc(String desc) {
		this.desc = desc;
	}

	public Double getChange() {
		return change;
	}

	public void setChange(Double change) {
		this.change = change;
	}

	public Integer getViewPer() {
		return viewPer;
	}

	public void setViewPer(Integer viewPer) {
		this.viewPer = viewPer;
	}

	public Integer getIsCollect() {
		return isCollect;
	}

	public void setIsCollect(Integer isCollect) {
		this.isCollect = isCollect;
	}

	public Integer getcCount() {
		return cCount;
	}

	public void setcCount(Integer cCount) {
		this.cCount = cCount;
	}
}
