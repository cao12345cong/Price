package com.ssy.price.entity;

import java.io.Serializable;

@SuppressWarnings("serial")
public class Season implements Serializable {
	Integer sepid;// 产品id
	String simgUrl;// 缩略图地址
	String sepname;// 品名
	String parea;// 主要产地
	String comeMonth;// 上市时间
	Integer cCount;// 评论数量
	Integer isCollect;// 是否收藏 0:未收藏;1:已收藏

	public Integer getSepid() {
		return sepid;
	}

	public void setSepid(Integer sepid) {
		this.sepid = sepid;
	}

	public String getSimgUrl() {
		return simgUrl;
	}

	public void setSimgUrl(String simgUrl) {
		this.simgUrl = simgUrl;
	}

	public String getSepname() {
		return sepname;
	}

	public void setSepname(String sepname) {
		this.sepname = sepname;
	}

	public String getParea() {
		return parea;
	}

	public void setParea(String parea) {
		this.parea = parea;
	}

	public String getComeMonth() {
		return comeMonth;
	}

	public void setComeMonth(String comeMonth) {
		this.comeMonth = comeMonth;
	}

	public Integer getcCount() {
		return cCount;
	}

	public void setcCount(Integer cCount) {
		this.cCount = cCount;
	}

	public Integer getIsCollect() {
		return isCollect;
	}

	public void setIsCollect(Integer isCollect) {
		this.isCollect = isCollect;
	}
}
